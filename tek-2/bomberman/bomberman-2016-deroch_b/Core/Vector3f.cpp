#include	"Vector3f.hh"

Vector3f::Vector3f() : x(0.0f), y(0.0f), z(0.0f)
{

}

Vector3f::Vector3f(float x, float y, float z) : x(x), y(y), z(z)
{

}

bool		Vector3f::operator==(Vector3f const & v)
{
  return (x == v.x && y == v.y && z == v.z ? true : false);
}

bool		Vector3f::operator!=(Vector3f const & v)
{
  return (x != v.x || y != v.y || z != v.z ? true : false);
}

